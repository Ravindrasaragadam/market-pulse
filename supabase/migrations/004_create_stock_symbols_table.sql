-- 4. Function to search stocks
CREATE OR REPLACE FUNCTION search_stocks(search_query TEXT)
RETURNS TABLE (
    symbol TEXT,
    name TEXT,
    sector TEXT,
    market_cap_category TEXT
) AS $$
BEGIN
    RETURN QUERY
    SELECT 
        s.symbol,
        s.name,
        s.sector,
        s.market_cap_category
    FROM stock_symbols s
    WHERE s.is_active = TRUE
        AND (
            s.symbol ILIKE '%' || search_query || '%'
            OR s.name ILIKE '%' || search_query || '%'
            OR s.sector ILIKE '%' || search_query || '%'
        )
    ORDER BY 
        CASE 
            WHEN s.symbol ILIKE search_query || '%' THEN 1
            WHEN s.name ILIKE search_query || '%' THEN 2
            ELSE 3
        END,
        s.market_cap_category = 'Large' DESC,
        s.symbol
    LIMIT 20;
END;
$$ LANGUAGE plpgsql;
