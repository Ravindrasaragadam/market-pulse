-- 1. Stock Symbols Table
CREATE TABLE IF NOT EXISTS stock_symbols (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    symbol TEXT NOT NULL UNIQUE,      -- NSE symbol like RELIANCE
    name TEXT NOT NULL,               -- Full company name
    sector TEXT,                      -- IT, Banking, Energy, etc.
    industry TEXT,                    -- Sub-sector
    exchange TEXT DEFAULT 'NSE',      -- NSE, BSE
    market_cap_category TEXT,         -- Large, Mid, Small cap
    is_active BOOLEAN DEFAULT TRUE,
    isin_code TEXT,                   -- ISIN for unique identification
    metadata JSONB,                   -- Extra data
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now()
);

-- Indexes for fast search
CREATE INDEX IF NOT EXISTS idx_stock_symbols_symbol ON stock_symbols(symbol);
CREATE INDEX IF NOT EXISTS idx_stock_symbols_name ON stock_symbols(name);
CREATE INDEX IF NOT EXISTS idx_stock_symbols_sector ON stock_symbols(sector);
CREATE INDEX IF NOT EXISTS idx_stock_symbols_active ON stock_symbols(is_active);

-- Full-text search index
CREATE INDEX IF NOT EXISTS idx_stock_symbols_search ON stock_symbols 
    USING gin(to_tsvector('english', coalesce(symbol, '') || ' ' || coalesce(name, '')));

-- 2. User Watchlist Table (for personalized tracking)
CREATE TABLE IF NOT EXISTS user_watchlist (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    user_id UUID DEFAULT '00000000-0000-0000-0000-000000000000'::UUID,  -- Default user for now
    stock_symbol TEXT NOT NULL REFERENCES stock_symbols(symbol) ON DELETE CASCADE,
    added_at TIMESTAMPTZ DEFAULT now(),
    priority INTEGER DEFAULT 5,       -- User-defined priority (1-10)
    notes TEXT,                     -- User notes
    is_active BOOLEAN DEFAULT TRUE,
    UNIQUE(user_id, stock_symbol)
);

CREATE INDEX IF NOT EXISTS idx_user_watchlist_user ON user_watchlist(user_id);
CREATE INDEX IF NOT EXISTS idx_user_watchlist_symbol ON user_watchlist(stock_symbol);
CREATE INDEX IF NOT EXISTS idx_user_watchlist_priority ON user_watchlist(priority DESC);

